#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : DisplayBinary()
//  Description      : Converts given decimal number into binary format and displays it.
//  Input            : Integer
//  Output           : Displays binary representation
//  Author           : Gavade Somanath Vilas
//  Date             : 16/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DisplayBinary(int iNo)
{
    int iDigit = 0, iCnt = 0, iSize = 0;
    int Arr[32];

    while(iNo != 0)
    {
        iDigit = iNo % 2;
        Arr[iCnt] = iDigit;
        iCnt++;

        iNo = iNo / 2;
    }

    iSize = iCnt;

    for(iCnt = iSize - 1; iCnt >= 0; iCnt--)
    {
        cout << Arr[iCnt] << "\t";
    }

    cout << "\n";
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Decimal to Binary Conversion
//  Description      : This program accepts a number and displays its binary representation.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    DisplayBinary(iValue);

    return 0;
}
#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Arithmetic
//  Description      : This class is used to perform arithmetic operations on two integer values.
//  Input            : Two integers
//  Output           : Integer
//  Author           : Gavade Somanath Vilas
//  Date             : 23/02/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Arithmetic
{
    public:
        int iValue1;
        int iValue2;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Arithmetic (Parameterized Constructor)
//  Description      : Initializes class data members with given values.
//  Input            : Two integers
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Arithmetic(int A, int B)
        {
            iValue1 = A;
            iValue2 = B;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Addition
//  Description      : This function returns addition of two integers.
//  Input            : None (Uses class data members)
//  Output           : Integer (Addition result)
//  Date             : 23/02/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        int Addition()
        {
            int iResult = 0;
            iResult = iValue1 + iValue2;
            return iResult;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Addition of Two Numbers using OOP
//  Description      : This application accepts two integers from user, creates object of Arithmetic class, and displays addition result.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iNo1 = 0, iNo2 = 0, iRet = 0;

    cout << "Enter First Number : \n";
    cin >> iNo1;

    cout << "Enter Second Number : \n";
    cin >> iNo2;

    // Object Creation
    Arithmetic aobj(iNo1, iNo2);

    iRet = aobj.Addition();        // Function Call

    cout << "Addition is : " << iRet << "\n";

    return 0;
}
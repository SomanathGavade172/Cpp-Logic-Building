#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Array
//  Description      : Contains functions to accept and display elements of dynamic array.
//  Input            : Integer values
//  Output           : Displays array elements
//  Author           : Gavade Somanath Vilas
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Array
{
    public:
        int *Arr;
        int iSize; 

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Array (Parameterized Constructor)
//  Description      : Allocates memory for array based on given size.
//  Input            : One integer (size of array)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Array(int A)
        {
            iSize = A;
            Arr = new int[iSize];            
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Destructor Name  : ~Array
//  Description      : Deallocates memory of dynamic array.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        ~Array()
        {
            delete []Arr;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Accept()
//  Description      : Accepts elements from user and stores them in array.
//  Input            : None
//  Output           : None
//  Time Complexity  : O(N)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Accept()
        {
            int iCnt = 0;

            cout << "Enter " << iSize << " elements : \n";

            for(iCnt = 0; iCnt < iSize; iCnt++)
            {
                cin >> Arr[iCnt];
            }
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display()
//  Description      : Displays all elements of array.
//  Input            : None
//  Output           : Prints array elements
//  Time Complexity  : O(N)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Display()
        {
            int iCnt = 0;

            cout << "Values from the array are : \n";

            for(iCnt = 0; iCnt < iSize; iCnt++)
            {
                cout << Arr[iCnt] << "\n";
            }
        }        
};  

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Dynamic Array using OOP
//  Description      : This program accepts number of elements from user, stores them in dynamic array, and displays them.
//  Input            : Integer values
//  Output           : Displays elements of array
//  Author           : Gavade Somanath Vilas
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLength = 0;

    cout << "Enter number of elements that you want to store : \n";
    cin >> iLength;

    Array aobj(iLength);

    aobj.Accept();

    aobj.Display();

    return 0;
}
#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Array
//  Description      : Contains functions to accept, display elements and count even numbers from dynamic array.
//  Input            : Integer values
//  Output           : Displays array elements and returns count of even elements
//  Author           : Gavade Somanath Vilas
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Array
{
    public:
        int *Arr;
        int iSize; 

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Array (Parameterized Constructor)
//  Description      : Allocates memory for array based on given size.
//  Input            : One integer (size of array)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Array(int A)
        {
            iSize = A;
            Arr = new int[iSize];            
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Destructor Name  : ~Array
//  Description      : Deallocates memory of dynamic array.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        ~Array()
        {
            delete []Arr;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Accept()
//  Description      : Accepts elements from user and stores them in array.
//  Input            : None
//  Output           : None
//  Time Complexity  : O(N)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Accept()
        {
            int iCnt = 0;

            cout << "Enter " << iSize << " elements : \n";

            for(iCnt = 0; iCnt < iSize; iCnt++)
            {
                cin >> Arr[iCnt];
            }
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display()
//  Description      : Displays all elements of array.
//  Input            : None
//  Output           : Prints array elements
//  Time Complexity  : O(N)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Display()
        {
            int iCnt = 0;

            cout << "Values from the array are : \n";

            for(iCnt = 0; iCnt < iSize; iCnt++)
            {
                cout << Arr[iCnt] << "\n";
            }
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CountEven()
//  Description      : Counts number of even elements from array.
//  Input            : None
//  Output           : Integer (count of even numbers)
//  Time Complexity  : O(N)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        int CountEven()
        {
            int iCnt = 0, iCount = 0;

            for(iCnt = 0; iCnt < iSize; iCnt++)
            {
                if(Arr[iCnt] % 2 == 0)
                {
                    iCount++;
                }
            }

            return iCount;
        }
};  

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Dynamic Array Even Count using OOP
//  Description      : This program accepts elements, displays them and counts number of even elements.
//  Input            : Integer values
//  Output           : Displays elements and count of even numbers
//  Author           : Gavade Somanath Vilas
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLength = 0, iRet = 0;

    cout << "Enter number of elements that you want to store : \n";
    cin >> iLength;

    Array aobj(iLength);

    aobj.Accept();

    aobj.Display();

    iRet = aobj.CountEven();

    cout << "Number of Even elements are : " << iRet << "\n";

    return 0;
}
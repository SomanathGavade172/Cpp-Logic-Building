#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display()
//  Description      : Displays each character of the string on new line.
//  Input            : Character pointer (string)
//  Output           : Prints characters of string
//  Time Complexity  : O(N)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(char *str)
{
    while(*str != '\0')
    {
        printf("%c\n", *str);
        str++;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : String Character Display Program
//  Description      : This program accepts a string from user and displays each character on new line.
//  Input            : String
//  Output           : Displays characters of string
//  Author           : Gavade Somanath Vilas
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];

    printf("Enter a string : \n");
    scanf("%[^'\n']s",Arr);

    Display(Arr);

    return 0;
}
#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Array
//  Description      : Contains functions to accept, display elements and reverse the array.
//  Input            : Integer values
//  Output           : Displays array elements and performs reverse operation
//  Author           : Gavade Somanath Vilas
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Array
{
    public:
        int *Arr;
        int iSize; 

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Array (Parameterized Constructor)
//  Description      : Allocates memory for array based on given size.
//  Input            : One integer (size of array)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Array(int A)
        {
            iSize = A;
            Arr = new int[iSize];            
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Destructor Name  : ~Array
//  Description      : Deallocates memory of dynamic array.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        ~Array()
        {
            delete []Arr;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Accept()
//  Description      : Accepts elements from user and stores them in array.
//  Input            : None
//  Output           : None
//  Time Complexity  : O(N)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Accept()
        {
            int iCnt = 0;

            cout << "Enter " << iSize << " elements : \n";

            for(iCnt = 0; iCnt < iSize; iCnt++)
            {
                cin >> Arr[iCnt];
            }
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display()
//  Description      : Displays all elements of array.
//  Input            : None
//  Output           : Prints array elements
//  Time Complexity  : O(N)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Display()
        {
            int iCnt = 0;

            cout << "Values from the array are : \n";

            for(iCnt = 0; iCnt < iSize; iCnt++)
            {
                cout << Arr[iCnt] << "\n";
            }
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Reverse()
//  Description      : Reverses the elements of the array using two-pointer technique.
//  Input            : None
//  Output           : None (modifies array in-place)
//  Time Complexity  : O(N/2)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        
        void Reverse()
        {
            int iStart = 0;
            int iEnd = iSize - 1;
            int Temp = 0;

            while(iStart < iEnd)
            {
                Temp = Arr[iStart];
                Arr[iStart] = Arr[iEnd];
                Arr[iEnd] = Temp;

                iStart++;
                iEnd--;
            }
        }
};  

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Dynamic Array Reverse using OOP
//  Description      : This program accepts elements, displays them, reverses the array and displays updated elements.
//  Input            : Integer values
//  Output           : Displays elements before and after reverse
//  Author           : Gavade Somanath Vilas
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLength = 0;

    cout << "Enter number of elements that you want to store : \n";
    cin >> iLength;

    Array aobj(iLength);

    aobj.Accept();

    aobj.Display();

    aobj.Reverse();

    cout << "Elements of array after reverse : \n";

    aobj.Display();

    return 0;
}
#include<iostream>
using namespace std;

class Number
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Number (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : One integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Number(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : DisplayFactors
//  Description      : Displays all factors of the stored integer except the number itself.
//  Input            : None (Uses class data member)
//  Output           : None (Prints factors on console)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        void DisplayFactors()
        {
            int iCnt = 0;

            for(iCnt = 1; iCnt <= (iNo / 2); iCnt++)
            {
                if(iNo % iCnt == 0)
                {
                    cout << iCnt << "\t";
                }
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Display Factors of a Number using OOP
//  Description      : This application accepts an integer from user, creates an object of Number class, and displays its factors.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    // Object Creation
    Number nobj(iValue);

    nobj.DisplayFactors();    // Proper OOP Function Call

    return 0;
}


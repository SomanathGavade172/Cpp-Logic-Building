#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display()
//  Description      : Displays each character of the string in reverse order on new line.
//  Input            : Character pointer (string)
//  Output           : Prints characters of string in reverse
//  Time Complexity  : O(N)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(char *str)
{
    char *p = str;

    while(*p != '\0')
    {
        p++;
    }

    p--;

    while(p >= str)
    {
        printf("%c\n", *p);
        p--;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : String Reverse Display Program
//  Description      : This program accepts a string from user and displays each character in reverse order.
//  Input            : String
//  Output           : Displays characters of string in reverse
//  Author           : Gavade Somanath Vilas
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];

    printf("Enter a string : \n");
    scanf("%[^\n]",Arr);

    Display(Arr);

    return 0;
}
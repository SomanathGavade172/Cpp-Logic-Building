#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Addition
//  Description      : This function accepts two integer values and returns their addition.
//  Input            : Integer, Integer
//  Output           : Integer (Addition of two numbers)
//  Author           : Gavade Somanath Vilas
//  Date             : 23/02/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Definition
int Addition(int iValue1, int iValue2)
{
    int iResult = 0;
    iResult = iValue1 + iValue2;
    return iResult;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Addition of Two Numbers
//  Description      : This application calls Addition function to calculate addition of two integers and displays the result.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iNo1 = 11, iNo2 = 10, iRet = 0;

    iRet = Addition(iNo1, iNo2);        // Function Call

    cout << "Addition is : " << iRet << "\n";

    return 0;
}
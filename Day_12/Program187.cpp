#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : DisplayFactors
//  Description      : Displays all factors of the given integer except the number itself.
//  Input            : Integer
//  Output           : None (Prints factors on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 23/02/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Definition
void DisplayFactors(int iNo)            // Time Complexity = O(N)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt < iNo; iCnt++)
    {
        if(iNo % iCnt == 0)
        {
            cout << iCnt << "\t";
        }
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Display Factors of a Number
//  Description      : This application accepts an integer from user and displays its factors.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    DisplayFactors(iValue);    // Function Call

    return 0;
}
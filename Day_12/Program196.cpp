#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Swap()
//  Description      : Swaps two integer values using pointers.
//  Input            : Two integer pointers
//  Output           : None (values are swapped using reference)
//  Time Complexity  : O(1)
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Swap(int *p, int *q)
{
    int Temp = 0;

    Temp = *p;
    *p = *q;
    *q = Temp;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Swap Two Numbers using Pointers
//  Description      : This program accepts two numbers from user and swaps their values using pointer technique.
//  Input            : Integer values
//  Output           : Displays swapped values
//  Author           : Gavade Somanath Vilas
//  Date             : 15/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iNo1 = 0, iNo2 = 0;

    cout << "Enter First number : \n";
    cin >> iNo1;

    cout << "Enter second number : \n";
    cin >> iNo2;

    Swap(&iNo1, &iNo2);

    cout << "Value of No1 after swapping : " << iNo1 << "\n";
    cout << "Value of No2 after swapping : " << iNo2 << "\n";

    return 0;
}